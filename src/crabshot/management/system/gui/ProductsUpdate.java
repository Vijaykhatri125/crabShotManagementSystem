    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        String name=this.nameField.getText();
        String cost=this.costField.getText();
        String price=this.priceField.getText();
        String quantity=this.quantityField.getText();
        String unit = this.unitOptions.getSelectedItem().toString();
        if(name.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter name");
        }
        else if(cost.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter cost of Product");
        }
        else if(price.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter price of Product");
        }
        else if(quantity.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter quantity of Product");
        }
        else if(unit.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter unit of Product");
        }
        else{
           // System.out.println("hello - else");
            int id=DBManager.getProductId(name);
            //System.out.println("id is : "+id);
            if(price.matches("[0-9]+([.][0-9]+)*") && quantity.matches("[0-9]+([.][0-9]+)*") && cost.matches("[0-9]+([.][0-9]+)*")){
                Double proCost=Double.parseDouble(this.costField.getText());
                Double proPrice=Double.parseDouble(this.priceField.getText());
                Double proQuantity=Double.parseDouble(this.quantityField.getText());
                if(unit.equals("kg")) {
                    proQuantity *= 1000;
                }
                boolean status=DBManager.updateProduct(name, proCost, proPrice, proQuantity,unit, id);
                if(status==true){
                    Products product=new Products();
                    product.setVisible(true);
                    setVisible(false);
                }
            }
            else if(!cost.matches("[0-9]+([.][0-9]+)*")){
                JOptionPane.showMessageDialog(null, "Please enter a valid number in Cost Field");
            }
            else if(!price.matches("[0-9]+([.][0-9]+)*")){
                JOptionPane.showMessageDialog(null, "Please enter a valid number in Price Field");
            }
            else if(!quantity.matches("[0-9]+([.][0-9]+)*")){
                JOptionPane.showMessageDialog(null, "Please enter a valid number in Quantity Field");
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

