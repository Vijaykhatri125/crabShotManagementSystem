    private void showRecord(String name) {
        try{
            ResultSet result=DBManager.getSearchedProductRecord(name);
            while(result.next()){
                this.nameField.setText(result.getString("name"));
                this.costField.setText(result.getString("cost"));
                this.priceField.setText(result.getString("price"));
                if(result.getString("unit").equals("kg"))
                    this.quantityField.setText((result.getDouble("quantity")/1000) + "");
                else
                    this.quantityField.setText(result.getString("quantity"));
                //this.unitOptions.setText(result.getString("unit"));
                this.unitOptions.setSelectedItem(result.getString("unit"));
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        unitOptions.addItemListener(new java.awt.event.ItemListener() {
            public void itemStateChanged(java.awt.event.ItemEvent evt) {
                unitOptionsItemStateChanged(evt);
            }
        });
    }
